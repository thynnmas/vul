#ifndef VUL_TYPES_H 
#define VUL_TYPES_H

// Floats
typedef float f32_t;
typedef double f64_t;

// Signed Integers
typedef char i8_t;
typedef short i16_t;
typedef int i32_t;
typedef long long i64_t;

// Unsigned integers
typedef unsigned char ui8_t;
typedef unsigned short ui16_t;
typedef unsigned int ui32_t;
typedef unsigned long long ui64_t;

#endif